// Initialize sidebar on any admin page
function initAdminSidebar(currentPage) {
  const sidebar = document.getElementById('admin-sidebar');
  if (sidebar) {
    sidebar.innerHTML = renderAdminSidebar();

    // Highlight current page
    const navItems = sidebar.querySelectorAll('.admin-nav-item');
    navItems.forEach(item => {
      if (item.dataset.page === currentPage) {
        item.classList.add('active');
      }
    });

    // Update user info now that sidebar elements exist
    if (typeof admin !== 'undefined' && admin.updateUserInfo) {
      admin.updateUserInfo();
    }
  }
}
